"""–ó–¥–µ—Å—å —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã, —Å–≤—è–∑–∞–Ω–Ω—ã–µ —Å —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º –æ–±—ä–µ–∫—Ç–æ–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–≤ –≤ –ë–î"""

from aiogram.types import InlineKeyboardMarkup, InlineKeyboardButton

import app.database.requests.administrators as rq

# Builder - –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –Ω–µ—Å—Ç–∞—Ç–∏—á–Ω—ã—Ö –∫–ª–∞–≤–∏–∞—Ç—É—Ä. –ü–æ–ª–µ–∑–µ–Ω –ø—Ä–∏ —Ä–∞–±–æ—Ç–µ —Å –±–∞–∑–∞–º–∏ –¥–∞–Ω–Ω—ã—Ö.
from aiogram.utils.keyboard import InlineKeyboardBuilder


# –°–æ–∑–¥–∞–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã —Å–æ —Å–ø–∏—Å–∫–æ–º –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–≤
async def administrators():
    admins = await rq.get_all_admins()
    keyboard = InlineKeyboardBuilder()
    for admin in admins:
        keyboard.add(InlineKeyboardButton(text=f'{admin.id}. {admin.name} {admin.surname}',
                                          callback_data=f'admin_{admin.id}'))
    keyboard.add(InlineKeyboardButton(text='–î–æ–±–∞–≤–∏—Ç—å –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ ‚ûï',
                                      callback_data=f'add_admin'))
    keyboard.add(InlineKeyboardButton(text='–ù–∞–∑–∞–¥ üîô', callback_data=f'adm_to_main'))
    return keyboard.adjust(1).as_markup()


# –°–æ–∑–¥–∞–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã —Å –∫–Ω–æ–ø–∫–æ–π, –≤–æ–∑–≤—Ä–∞—â–∞—é—â–µ–π –≤ –º–µ–Ω—é —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
async def back_to_admin(admin_id):
    admin = await rq.get_admin_by_id(admin_id)
    keyboard = InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text='–ö –æ–∫–Ω—É –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ üë®', callback_data=f'admin_{admin.id}')]
    ])
    return keyboard
